import sys

def f(l, m, n):
    return (0 < 30 + m and min(l, l) - max(-1, -1) > 1 or ((min(l, 9) + 1 > 1 or l - max(9, -1) > 1) or (-44 + -1 * n > -31 and -44 + -1 * n < 31 * l))) or (0 > -1 * n + -24 * l and min(l, l) - max(-1, -1) > 1)
if __name__ == '__main__':
    for line in sys.stdin:
        inputs = list(map(int, line.split(' ')))
        print(f(*inputs))